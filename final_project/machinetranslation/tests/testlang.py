def frenchToEnglish(test):
    """
    This function translates French to English
    """
    if test == "Bonjour":
        print("Hello")
frenchToEnglish(test="Bonjour")

def englisToFrench(test):
    """
    This function translates English to French
    """
    if test == "Hello":
        print("Bonjour")
englisToFrench(test="Hello")